def area_of_circle(radius):
    pie = 3.14
    area = pie * radius * radius
    return area

sword_length = 1.0
spear_length = 2.0

sword_area = area_of_circle(sword_length)
spear_area = area_of_circle(spear_length)

print(f"Sword area is ${sword_area}")
